/**
 * VENTURR VALIDT - Report Engine v1.0
 * Produces two outputs:
 * 1. VALIDT Verification Report (client-friendly)
 * 2. VALIDT Evidence / Court Report (defensible, clause-driven)
 * 
 * Each section includes:
 * - Findings table
 * - Evidence list (citations)
 * - Assumptions
 * - Confidence score
 * - "What evidence is still required" checklist
 */

import { Citation, Finding, AuditLogEntry, containsBlockedLanguage } from '../shared/citations';
import { citeOrBlockMiddleware, ConflictingSource } from './citeOrBlockMiddleware';
import { getAuditTrail } from './auditTrail';
import { COMPLIANCE_RULES, getRuleById } from './complianceKnowledgeBase';

export interface ReportSection {
  id: string;
  title: string;
  category: "pricing" | "materials" | "compliance" | "warranty" | "summary";
  findings: Finding[];
  score: number;
  confidence: "high" | "medium" | "low";
  assumptions: string[];
  evidenceRequired: string[];
  citations: Citation[];
}

export interface ClientReport {
  reportId: string;
  reportType: "client" | "court";
  generatedAt: string;
  quoteId: number;
  projectAddress?: string;
  contractorName?: string;
  
  // Summary
  overallScore: number;
  overallStatus: "green" | "amber" | "red";
  overallConfidence: "high" | "medium" | "low";
  executiveSummary: string;
  
  // Sections
  sections: ReportSection[];
  
  // Recommendations
  recommendations: Array<{
    priority: "high" | "medium" | "low";
    title: string;
    description: string;
    relatedFindings: string[];
  }>;
  
  // Warnings & Disclaimers
  warnings: string[];
  disclaimer: string;
  
  // Metadata
  analysisVersion: string;
  standardsReferenced: string[];
}

export interface CourtReport extends Omit<ClientReport, 'reportType'> {
  reportType: "court";
  
  // Additional court-specific sections
  auditTrail: AuditLogEntry[];
  conflictingSources: ConflictingSource[];
  
  // Detailed evidence chain
  evidenceChain: Array<{
    step: number;
    action: string;
    timestamp: string;
    input: string;
    output: string;
    confidence: string;
  }>;
  
  // Methodology statement
  methodology: {
    extractionMethod: string;
    analysisModel: string;
    standardsVersion: string;
    validationSteps: string[];
  };
  
  // Legal notices
  legalNotices: string[];
  expertWitnessStatement: string;
}

